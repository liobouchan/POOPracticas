import java.math.*;
public class ProbadorDeTiposDeDato{
		private static byte a=127;
		private static short b=-32768;
		private  static int c=2147483647;
		private static long d=-9223372036854775808l;
		private static float e=5.98f;
		private static double f= Math.PI;
		private static char g='a';
		private static boolean h=true;
	public static void main(String args[]){
		System.out.println("Este programa mostrara ejemplos de los tipos de datos primitivos en java");
		System.out.println("Este es un valor tipo byte:");
		System.out.println(a);
		System.out.println("Este es un valor tipo short:");
		System.out.println(b);
		System.out.println("Este es un valor tipo entero(int):");
		System.out.println(c);
		System.out.println("Este es un valor tipo long:");
		System.out.println(d);
		System.out.println("Este es un valor tipo flotante(float):");
		System.out.println(e);
		System.out.println("Este es un valor tipo double:");
		System.out.println(f);
		System.out.println("Este es un valor tipo char:");
		System.out.println(g);
		System.out.println("Este es un valor tipo booleano(boolean):");
		System.out.println(h);
		
	}



}

